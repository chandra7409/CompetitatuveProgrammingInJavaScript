let num = -29;
switch (true) {
    case (num > 0):
        console.log("Positive number");
        break;
    case (num < 0):
        console.log("Negative number");
        break;
    case (num == 0):
        console.log("Zero number");
        break;
    default:
        console.log("Not a number");
        break;
}